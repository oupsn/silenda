package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

var cfgFile string

// rootCmd represents the base command when called without any subcommands
var RootCmd = &cobra.Command{
	Use:   "silenda",
	Short: "A CLI tool for managing secrets and environment variables, allowing you to set, view, and manage sensitive data",
	Long:  "Silenda provides a user-friendly and secure way to handle secrets and environment variables from the command line. It addresses the security concerns associated with storing sensitive information directly in scripts or environment files.",
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := RootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	RootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}

